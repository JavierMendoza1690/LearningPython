
#* list slices
# un list slice es literalment mostrar un tramo de la lista la sintaxis se ve en el siguiente ejemplo, el limite final 
# nunca est√° incluido

#inicializando la lista
monday_temperatures = [9.1, 8.8, 7.5, 6.6, 9.9];

#mostrando la lista desde el elemento 1 al 2
print('slice del 1 al 3', monday_temperatures[1:3])

#forma reducida del indice cero al dos
print('slice del 0 al 1', monday_temperatures[:2]);

#mostrando del 3 al 4
print('slice del 0 al 1', monday_temperatures[3:5]);